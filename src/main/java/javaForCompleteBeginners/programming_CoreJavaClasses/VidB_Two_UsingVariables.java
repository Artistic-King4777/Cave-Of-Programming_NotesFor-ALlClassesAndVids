        int myNumber = 88;
        short myShort = 847;
        long myLong = 9797;

        double myDouble = 7.3243;
        float myFloat = 324.3f;

        char myChar = 'y';
        boolean myBoolean = false;

        byte myByte = 127;

        System.out.println(myNumber);
        System.out.println(myShort);
