        /**
         * 8 primitive variable types
         *
         * 1. int = 32 bit value -- whole number
         * 2. short = 16 bit value (can maybe go up to 32,000)
         * 3. long = 64 bit value
         *
         *
         * 4. double = floating point (extra precision)
         * 5. float = short vers of double (if you type
         *     a floating point number with a decimal it is by default
         *     a double so you must add an 'f' to the end
         *
         *
         * 6. char = holds a unicode character using single quotes '@'
         * 7. boolean = holds true or false
         *
         *
         * 8. byte = 8 bit value = negative or positive can only
         *     go to 127
         */



        // ----------------------------------------------------

        // My Code:

        int mini = 13;
